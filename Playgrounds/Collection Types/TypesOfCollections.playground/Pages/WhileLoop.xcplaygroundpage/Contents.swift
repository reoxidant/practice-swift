import Foundation

// While loop

/*:

    while condition {
        some code
    }

 */

var count = 6
while count > 0 {
    print("hello")
    print(count)
    count -= 1
}

count = -1

while count > 0 {
    print(count)
    count -= 1
}

/*:
    repeat {
        some code
    } while condition

 */

count = -1

repeat {
    print(count)
} while count > 0

